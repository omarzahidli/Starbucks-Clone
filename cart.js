function deleteCart(elm) {
    let cart = JSON.parse(localStorage.getItem('cart'))
    cart.splice(elm, 1);
    localStorage.setItem('cart', JSON.stringify(cart))
    renderBasket()
}

function addCart(elm) {
    let cart = JSON.parse(localStorage.getItem('cart'))
    let copy = cart[elm];
    cart.push(copy)
    localStorage.setItem('cart', JSON.stringify(cart))
    renderBasket()
}